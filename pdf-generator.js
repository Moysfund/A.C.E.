// pdf-generator.js - PDF Report Generation

// Load jsPDF from CDN
function loadPDFLibrary() {
    return new Promise((resolve) => {
        if (typeof window.jspdf !== 'undefined') {
            resolve(window.jspdf);
            return;
        }
        const script = document.createElement('script');
        script.src = 'https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js';
        script.onload = () => resolve(window.jspdf);
        document.head.appendChild(script);
    });
}

// Generate Stage Report PDF
async function generateStageReportPDF(data) {
    await loadPDFLibrary();
    const { jsPDF } = window.jspdf;
    const doc = new jsPDF('p', 'mm', 'a4');
    const pageWidth = 210;
    const margin = 20;
    let y = 20;

    // Header
    doc.setFillColor(0, 11, 26);
    doc.rect(0, 0, pageWidth, 40, 'F');
    
    doc.setFontSize(24);
    doc.setTextColor(0, 212, 255);
    doc.text('⚡ A.C.E.', margin, 25);
    
    doc.setFontSize(10);
    doc.setTextColor(200, 200, 200);
    doc.text('Automated Compliance Engine', margin + 50, 28);
    
    doc.setFontSize(8);
    doc.setTextColor(150, 150, 150);
    doc.text('LASBCA · Oba Akinjobi Way, Ikeja', margin, 35);

    // Title
    y = 55;
    doc.setFontSize(18);
    doc.setTextColor(255, 255, 255);
    doc.text('STAGE REPORT', margin, y);
    
    doc.setDrawColor(0, 212, 255);
    doc.setLineWidth(0.5);
    doc.line(margin, y + 3, pageWidth - margin, y + 3);

    // Report ID
    y += 12;
    doc.setFontSize(10);
    doc.setTextColor(150, 150, 150);
    doc.text(`Report ID: ${data.id || 'SR-2026-001'}`, margin, y);
    doc.text(`Date: ${data.date || new Date().toLocaleDateString()}`, pageWidth - margin - 40, y);

    // Project Details
    y += 12;
    doc.setFontSize(12);
    doc.setTextColor(0, 212, 255);
    doc.text('PROJECT INFORMATION', margin, y);
    
    y += 6;
    doc.setFontSize(10);
    doc.setTextColor(200, 200, 200);
    const fields = [
        ['Project:', data.project || '-'],
        ['Developer:', data.developer || '-'],
        ['Permit #:', data.permit || '-'],
        ['Address:', data.address || '-'],
        ['Stage:', data.stage || '-'],
        ['Team:', data.team || '-'],
        ['Status:', data.status || 'Pending']
    ];
    
    fields.forEach(([label, value]) => {
        doc.text(`${label}`, margin, y);
        doc.text(`${value}`, margin + 45, y);
        y += 7;
    });

    // Observations
    y += 5;
    doc.setFontSize(12);
    doc.setTextColor(0, 212, 255);
    doc.text('AI OBSERVATIONS', margin, y);
    
    y += 6;
    doc.setFontSize(10);
    doc.setTextColor(200, 200, 200);
    const observations = data.observations || [
        '✅ Structural integrity: Nominal',
        '⚠️ Rebar spacing: Check column 3',
        '✅ Concrete curing: In progress'
    ];
    observations.forEach(obs => {
        const lines = doc.splitTextToSize(obs, pageWidth - margin * 2);
        lines.forEach(line => {
            doc.text(line, margin, y);
            y += 6;
        });
    });

    // Footer
    doc.setDrawColor(100, 100, 100);
    doc.setLineWidth(0.2);
    doc.line(margin, 270, pageWidth - margin, 270);
    
    doc.setFontSize(8);
    doc.setTextColor(150, 150, 150);
    doc.text('Generated by A.C.E. - Automated Compliance Engine', margin, 280);
    doc.text('This is a system-generated report.', margin, 286);

    // Save PDF
    doc.save(`${data.id || 'Stage-Report'}-${new Date().toISOString().split('T')[0]}.pdf`);
    return doc;
}

